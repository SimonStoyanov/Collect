                @TrackedDevicePosition.canceled -= m_Wrapper.m_UIActionsCallbackInterface.OnTrackedDevicePosition;
                @TrackedDeviceRotation.started -= m_Wrapper.m_UIActionsCallbackInterface.OnTrackedDeviceRotation;
                @TrackedDeviceRotation.performed -= m_Wrapper.m_UIActionsCallbackInterface.OnTrackedDeviceRotation;
                @TrackedDeviceRotation.canceled -= m_Wrapper.m_UIActionsCallbackInterface.OnTrackedDeviceRotation;
            }
            m_Wrapper.m_UIActionsCallbackInterface = instance;
            if (instance != null)
            {
                @Navigate.started += instance.OnNavigate;
                @Navigate.performed += instance.OnNavigate;
                @Navigate.canceled += instance.OnNavigate;
                @Submit.started += instance.OnSubmit;
                @Submit.performed += instance.OnSubmit;
                @Submit.canceled += instance.OnSubmit;
                @Cancel.started += instance.OnCancel;
                @Cancel.performed += instance.OnCancel;
